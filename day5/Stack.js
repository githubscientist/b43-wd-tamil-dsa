/*
    Stack 
        - Data Structure
        - Abstract Data Type
        - behaviour: LIFO (Last In First Out) or FILO (First In Last Out)
        - Last inserted element gets deleted first or first inserted element gets deleted last
        - insertion: O(1)
        - deletion: O(1)
        - accessing the topmost element: O(1)
        - Operations:
            1. Push: pushes the element to the stack
            2. Pop: deletes the element from the stack(topmost)
            3. Peek: returns the topmost element from the stack
            4. isEmpty: returns true if the stack is empty and false otherwise

        - stackoverflow, stackunderflow, isFull, ...

        - Implementation:
            1. Array Implementation of Stack
            2. Object Implementation of Stack
*/